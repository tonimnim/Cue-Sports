import '../../domain/entities/community_event.dart';

/// Model class for community events
class CommunityEventModel extends CommunityEvent {
  const CommunityEventModel({
    required String id,
    required String communityId,
    required String title,
    required String description,
    required String organizerId,
    String? createdBy,
    required DateTime startTime,
    required DateTime endTime,
    DateTime? startDateTime,
    DateTime? endDateTime,
    required EventType type,
    EventType? eventType,
    EventStatus status = EventStatus.upcoming,
    required String venue,
    required String venueAddress,
    String? location,
    required int maxParticipants,
    required List<String> participants,
    List<String>? registeredParticipants,
    required List<String> waitlist,
    required bool isPrivate,
    required bool requiresRegistration,
    bool isActive = true,
    double? entryFee,
    double? prizePool,
    Map<String, dynamic>? prizes,
    String? coverImageUrl,
    String? imageUrl,
    required DateTime createdAt,
    DateTime? updatedAt,
  }) : super(
          id: id,
          communityId: communityId,
          title: title,
          description: description,
          organizerId: organizerId,
          createdBy: createdBy,
          startTime: startTime,
          endTime: endTime,
          startDateTime: startDateTime,
          endDateTime: endDateTime,
          type: type,
          eventType: eventType,
          status: status,
          venue: venue,
          venueAddress: venueAddress,
          location: location,
          maxParticipants: maxParticipants,
          participants: participants,
          registeredParticipants: registeredParticipants,
          waitlist: waitlist,
          isPrivate: isPrivate,
          requiresRegistration: requiresRegistration,
          isActive: isActive,
          entryFee: entryFee,
          prizePool: prizePool,
          prizes: prizes,
          coverImageUrl: coverImageUrl,
          imageUrl: imageUrl,
          createdAt: createdAt,
          updatedAt: updatedAt,
        );

  /// Create a CommunityEventModel from a map
  factory CommunityEventModel.fromJson(Map<String, dynamic> json) {
    return CommunityEventModel(
      id: json['id'] as String,
      communityId: json['communityId'] as String,
      title: json['title'] as String,
      description: json['description'] as String,
      organizerId: json['organizerId'] as String,
      createdBy: json['createdBy'] as String?,
      startTime: DateTime.parse(json['startTime'] as String),
      endTime: DateTime.parse(json['endTime'] as String),
      startDateTime: json['startDateTime'] != null
          ? DateTime.parse(json['startDateTime'] as String)
          : null,
      endDateTime: json['endDateTime'] != null
          ? DateTime.parse(json['endDateTime'] as String)
          : null,
      type: EventType.values.firstWhere(
        (e) => e.toString().split('.').last == json['type'],
        orElse: () => EventType.other,
      ),
      eventType: json['eventType'] != null
          ? EventType.values.firstWhere(
              (e) => e.toString().split('.').last == json['eventType'],
              orElse: () => EventType.other,
            )
          : null,
      status: EventStatus.values.firstWhere(
        (e) => e.toString().split('.').last == json['status'],
        orElse: () => EventStatus.upcoming,
      ),
      venue: json['venue'] as String,
      venueAddress: json['venueAddress'] as String,
      location: json['location'] as String?,
      maxParticipants: json['maxParticipants'] as int,
      participants: List<String>.from(json['participants'] as List? ?? []),
      registeredParticipants: json['registeredParticipants'] != null
          ? List<String>.from(json['registeredParticipants'] as List)
          : null,
      waitlist: List<String>.from(json['waitlist'] as List? ?? []),
      isPrivate: json['isPrivate'] as bool? ?? false,
      requiresRegistration: json['requiresRegistration'] as bool? ?? false,
      isActive: json['isActive'] as bool? ?? true,
      entryFee: (json['entryFee'] as num?)?.toDouble(),
      prizePool: (json['prizePool'] as num?)?.toDouble(),
      prizes: json['prizes'] as Map<String, dynamic>?,
      coverImageUrl: json['coverImageUrl'] as String?,
      imageUrl: json['imageUrl'] as String?,
      createdAt: DateTime.parse(json['createdAt'] as String),
      updatedAt: json['updatedAt'] != null
          ? DateTime.parse(json['updatedAt'] as String)
          : null,
    );
  }

  /// Create a CommunityEventModel from a CommunityEvent entity
  factory CommunityEventModel.fromEntity(CommunityEvent entity) {
    return CommunityEventModel(
      id: entity.id,
      communityId: entity.communityId,
      title: entity.title,
      description: entity.description,
      organizerId: entity.organizerId,
      createdBy: entity.createdBy,
      startTime: entity.startTime,
      endTime: entity.endTime,
      startDateTime: entity.startDateTime,
      endDateTime: entity.endDateTime,
      type: entity.type,
      eventType: entity.eventType,
      status: entity.status,
      venue: entity.venue,
      venueAddress: entity.venueAddress,
      location: entity.location,
      maxParticipants: entity.maxParticipants,
      participants: entity.participants,
      registeredParticipants: entity.registeredParticipants,
      waitlist: entity.waitlist,
      isPrivate: entity.isPrivate,
      requiresRegistration: entity.requiresRegistration,
      isActive: entity.isActive,
      entryFee: entity.entryFee,
      prizePool: entity.prizePool,
      prizes: entity.prizes,
      coverImageUrl: entity.coverImageUrl,
      imageUrl: entity.imageUrl,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
    );
  }

  /// Convert to entity (since this extends CommunityEvent, it can be used directly)
  CommunityEvent toEntity() => this;

  /// Convert the CommunityEventModel to a map
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'communityId': communityId,
      'title': title,
      'description': description,
      'organizerId': organizerId,
      'createdBy': createdBy,
      'startTime': startTime.toIso8601String(),
      'endTime': endTime.toIso8601String(),
      'startDateTime': startDateTime?.toIso8601String(),
      'endDateTime': endDateTime?.toIso8601String(),
      'type': type.toString().split('.').last,
      'eventType': eventType?.toString().split('.').last,
      'status': status.toString().split('.').last,
      'venue': venue,
      'venueAddress': venueAddress,
      'location': location,
      'maxParticipants': maxParticipants,
      'participants': participants,
      'registeredParticipants': registeredParticipants,
      'waitlist': waitlist,
      'isPrivate': isPrivate,
      'requiresRegistration': requiresRegistration,
      'isActive': isActive,
      'entryFee': entryFee,
      'prizePool': prizePool,
      'prizes': prizes,
      'coverImageUrl': coverImageUrl,
      'imageUrl': imageUrl,
      'createdAt': createdAt.toIso8601String(),
      'updatedAt': updatedAt?.toIso8601String(),
    };
  }

  /// Create a copy of this model with some fields updated
  CommunityEventModel copyWith({
    String? id,
    String? communityId,
    String? title,
    String? description,
    String? organizerId,
    String? createdBy,
    DateTime? startTime,
    DateTime? endTime,
    DateTime? startDateTime,
    DateTime? endDateTime,
    EventType? type,
    EventType? eventType,
    EventStatus? status,
    String? venue,
    String? venueAddress,
    String? location,
    int? maxParticipants,
    List<String>? participants,
    List<String>? registeredParticipants,
    List<String>? waitlist,
    bool? isPrivate,
    bool? requiresRegistration,
    bool? isActive,
    double? entryFee,
    double? prizePool,
    Map<String, dynamic>? prizes,
    String? coverImageUrl,
    String? imageUrl,
    DateTime? createdAt,
    DateTime? updatedAt,
  }) {
    return CommunityEventModel(
      id: id ?? this.id,
      communityId: communityId ?? this.communityId,
      title: title ?? this.title,
      description: description ?? this.description,
      organizerId: organizerId ?? this.organizerId,
      createdBy: createdBy ?? this.createdBy,
      startTime: startTime ?? this.startTime,
      endTime: endTime ?? this.endTime,
      startDateTime: startDateTime ?? this.startDateTime,
      endDateTime: endDateTime ?? this.endDateTime,
      type: type ?? this.type,
      eventType: eventType ?? this.eventType,
      status: status ?? this.status,
      venue: venue ?? this.venue,
      venueAddress: venueAddress ?? this.venueAddress,
      location: location ?? this.location,
      maxParticipants: maxParticipants ?? this.maxParticipants,
      participants: participants ?? this.participants,
      registeredParticipants: registeredParticipants ?? this.registeredParticipants,
      waitlist: waitlist ?? this.waitlist,
      isPrivate: isPrivate ?? this.isPrivate,
      requiresRegistration: requiresRegistration ?? this.requiresRegistration,
      isActive: isActive ?? this.isActive,
      entryFee: entryFee ?? this.entryFee,
      prizePool: prizePool ?? this.prizePool,
      prizes: prizes ?? this.prizes,
      coverImageUrl: coverImageUrl ?? this.coverImageUrl,
      imageUrl: imageUrl ?? this.imageUrl,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
    );
  }
} 